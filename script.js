const { en, ru } = data;
const body = document.querySelector("body");
const h1 = document.createElement("h1");
const nouteBook = document.createElement("div");
const screen = document.createElement("div");
const keyboard = document.createElement("div");
h1.innerHTML = "RSS Virtual Keyboard";
nouteBook.setAttribute("class", "noute-book");
screen.setAttribute("class", "screen");
keyboard.setAttribute("class", "keyboard");


function main() {
    for (let i = 0; i < en.length; i++) {
        const key = document.createElement("div");
        key.setAttribute("class", `${en[i].class}`);
        key.setAttribute("id", `${en[i].id}`);
        key.innerHTML = en[i].key;

        keyboard.append(key);
    }
    nouteBook.append(screen, keyboard);
    body.append(h1, nouteBook);


    const keys = document.querySelectorAll(".key");
    document.addEventListener("keydown", (e) => {
        // console.log(e.code);
        if (e.key === "Tab") {
            e.preventDefault();
        }

        keys.forEach((el) => {
            const keyCode = el.getAttribute("id");

            if (keyCode === e.code) {
                console.log(keyCode);
                el.classList.add("press");
            }
        });

    });
    document.addEventListener("keyup", (e) => {
        keys.forEach((el) => {
            const keyCode = el.getAttribute("id");
            if (keyCode === e.code) {
                console.log(keyCode);
                console.log(e);
                el.classList.remove("press");
                el.classList.add("press-up");
                setTimeout(() => {
                    el.classList.remove("press-up");
                }, 800);
            }
            // el.classList.remove("press-ap");
        });
    });
}
main();

